			<p><span class="dropcap">T</span>he St. James Calendar of the Christian Year reflects the common, ecumenical heritage that Christians share in the feast days and saints of the early, undivided Church, as it is expressed in the calendars of the Western and Eastern churches.</p>

			<p>Pride of place in any Christian calendar, of course, belongs to the commemorations of the life of Our Lord Jesus Christ—the Annunciation, Nativity, Baptism, Triumphal Entry, Crucifixion, -Resurrection, and Ascension, among others.</p>

			<p>But unlike many Christian calendars, the St. James Calendar also lists saints of both the Old and New -Testaments—prophets, apostles, evangelists and many -others—on their dates as -commemorated in the Eastern and Western churches.</p>

			<p>Furthermore, and uniquely, the St. James Calendar lists saints from antiquity who are commemorated on the same dates in both Eastern and Western calendars. There are hundreds of such saints shared by East and West throughout the year.</p>

		</div>
		<div class="col-sm-4">
			
			<p>This unique calendar is thus an -expression and daily reminder of the “communion of the saints” to which all Christians belong. It includes prophets, apostles, martyrs, missionaries, monks, children, married and celibate men and women, -peasants and kings, preachers, bishops, and pastors—all -members of the mystical Body of Christ, a “great cloud of witnesses.” </p>

			<p>The St. James Calendar is a modest attempt to present a rich treasure of our Christian heritage, biblically grounded, integral to the life of the early Church, and preserved in the liturgical traditions of various churches. It is hoped that this calendar will encourage a deeper appreciation of the Christian year and its message of salvation in the Lord Jesus Christ.</p>

 			<p>Engravings for the calendar months are selected in part to correspond to the Bible readings in the St. James Daily Devotional Guide for the Christian Year, edited by Patrick Henry Reardon, who also selected the monthly quotations from the church fathers to accompany the calendar illustrations. </p>

